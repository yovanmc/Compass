using System.Collections.ObjectModel;
using System.Threading;
using CommunityToolkit.Mvvm.ComponentModel;
using CommunityToolkit.Mvvm.Input;
using Compass.Core.Config;
using Compass.Core.Covers;
using Compass.Core.Model;
using Compass.Core.Sync;
using Compass.Core.Taste;

namespace Compass.App.ViewModels;

public sealed partial class LibraryViewModel : ObservableObject
{
    private readonly ISyncStore _store;
    private readonly RecommendationService _recs;
    private readonly RecommenderConfig _cfg;
    private readonly ICoverProvider _covers;

    private IReadOnlyList<Game> _library = [];
    private IReadOnlyDictionary<int, double> _scoreByAppId = new Dictionary<int, double>();
    private CancellationTokenSource _coverCts = new();

    // ── Displayed rows ─────────────────────────────────────────────────────
    public ObservableCollection<GameRow> Rows { get; } = new();

    // ── Filter / sort state ────────────────────────────────────────────────
    [ObservableProperty]
    private string searchText = string.Empty;

    [ObservableProperty]
    private LibraryStatus selectedStatus = LibraryStatus.All;

    [ObservableProperty]
    private string? selectedFacet;

    [ObservableProperty]
    private LibrarySort selectedSort = LibrarySort.Name;

    [ObservableProperty]
    private bool isPosterView;

    // ── Selection (detail panel — Phase 7) ────────────────────────────────
    [ObservableProperty]
    private GameRow? selectedRow;

    /// <summary>Raised when the user selects a row to view detail. Carries the SteamAppId.</summary>
    public event Action<int>? GameChosen;

    // ── Static combo-box sources ───────────────────────────────────────────
    public IReadOnlyList<LibraryStatus> Statuses { get; } =
        Enum.GetValues<LibraryStatus>();

    public IReadOnlyList<LibrarySort> Sorts { get; } =
        Enum.GetValues<LibrarySort>();

    public ObservableCollection<FacetOption> Facets { get; } = new();

    // ── Constructor ────────────────────────────────────────────────────────
    public LibraryViewModel(ISyncStore store, RecommendationService recs, CompassOptions opts, ICoverProvider covers)
    {
        _store  = store;
        _recs   = recs;
        _cfg    = opts.Recommender;
        _covers = covers;
        RefreshFromStore();
    }

    // ── Partial property-change hooks (generated by ObservableProperty) ────
    partial void OnSearchTextChanged(string value)           => ApplyFilter();
    partial void OnSelectedStatusChanged(LibraryStatus value) => ApplyFilter();
    partial void OnSelectedFacetChanged(string? value)       => ApplyFilter();
    partial void OnSelectedSortChanged(LibrarySort value)    => ApplyFilter();

    partial void OnSelectedRowChanged(GameRow? value)
    {
        if (value is not null)
            GameChosen?.Invoke(value.AppId);
    }

    /// <summary>Invoked by poster-view card clicks to open the detail panel.</summary>
    [RelayCommand]
    private void OpenDetail(GameRow row)
        => GameChosen?.Invoke(row.AppId);

    // ── Public API ─────────────────────────────────────────────────────────
    /// <summary>Called by ShellViewModel after a successful sync.</summary>
    public void RefreshFromStore()
    {
        _library = _store.LoadLibrary();

        // Compute scores for the whole library (backlog candidates get scored)
        var result = _recs.Recommend(_library, _cfg);
        _scoreByAppId = result.Recommendations
            .ToDictionary(r => r.Game.SteamAppId, r => r.Score);

        RebuildFacets();
        ApplyFilter();
    }

    // ── Private helpers ────────────────────────────────────────────────────
    private void RebuildFacets()
    {
        // Collect distinct genre: and theme: keys from the whole library.
        var seen = new HashSet<string>(StringComparer.Ordinal);
        var options = new List<FacetOption> { new("Any genre / theme", null) };

        foreach (var game in _library)
        {
            foreach (var key in game.FeatureKeys)
            {
                if (!key.StartsWith("genre:", StringComparison.Ordinal) &&
                    !key.StartsWith("theme:", StringComparison.Ordinal))
                    continue;

                if (!seen.Add(key)) continue;

                // Humanize: strip prefix, title-case.
                int colonIdx = key.IndexOf(':');
                string raw   = colonIdx >= 0 ? key[(colonIdx + 1)..] : key;
                string label = System.Globalization.CultureInfo.InvariantCulture
                    .TextInfo.ToTitleCase(raw.Replace('-', ' '));

                options.Add(new FacetOption(label, key));
            }
        }

        // Sort human labels alphabetically (after the "Any" entry).
        options.Sort(1, options.Count - 1,
            Comparer<FacetOption>.Create((a, b) =>
                string.Compare(a.Label, b.Label, StringComparison.OrdinalIgnoreCase)));

        Facets.Clear();
        foreach (var opt in options)
            Facets.Add(opt);
    }

    private async void LoadCoverForRow(GameRow row, CancellationToken ct)
    {
        // Called on the UI thread (from ApplyFilter which runs on UI thread).
        // The await without ConfigureAwait(false) resumes on the UI SynchronizationContext,
        // so the property set is marshaled correctly.
        try
        {
            var path = await _covers.GetCoverPathAsync(row.AppId, ct);
            if (!ct.IsCancellationRequested)
                row.CoverPath = path;
        }
        catch (OperationCanceledException) { /* stale load — ignore */ }
        catch { /* cover failure — placeholder stays */ }
    }

    private void ApplyFilter()
    {
        var filter = new LibraryFilter
        {
            Search              = SearchText,
            Status              = SelectedStatus,
            Facet               = SelectedFacet,
            Sort                = SelectedSort,
            PlayedFloorMinutes  = _cfg.PlayedFloorMinutes,
        };

        var filtered = LibraryQuery.Apply(_library, filter, _scoreByAppId);

        Rows.Clear();
        foreach (var g in filtered)
        {
            double score = _scoreByAppId.TryGetValue(g.SteamAppId, out var s) ? s : 0;
            Rows.Add(GameRow.From(g, score, _cfg.PlayedFloorMinutes));
        }

        // Cancel stale cover loads and start new ones for the current rows.
        _coverCts.Cancel();
        _coverCts = new CancellationTokenSource();
        var ct = _coverCts.Token;

        // Snapshot current rows to avoid capturing the live collection in async lambdas.
        var rowSnapshot = Rows.ToList();
        foreach (var row in rowSnapshot)
        {
            LoadCoverForRow(row, ct);
        }
    }
}
